"""Self-contained HTML report generator. Reads classify (and optional
basecomp) JSONs and emits a single HTML file with embedded base64 PNGs,
plus mechanism legend, executive summary, and per-bucket tables.

No JS, no CDN, no external CSS. `string.Template` + stdlib base64."""

import base64
import os
from html import escape
from string import Template

from tecap import __version__
from tecap.constants import (
    BUCKET_COLORS,
    BUCKET_DEFINITIONS,
    BUCKETS,
    CATEGORIES,
    COLORS,
    MECH_A_CORRECT,
    MECH_B_APA,
    MECHANISM_DEFINITIONS,
    PLOT_CAPTIONS,
    SCHEMA_VERSION,
)
from tecap.io import read_json
from tecap.plotting import (
    plot_comparison_basecomp,
    plot_comparison_classify,
    plot_histograms_basecomp,
)

